package de.ufinke.cubaja.sql.test.basic;

import java.util.*;

public class Data {

  private int intField;
  private double decimalField;
  private char charField;
  private String stringField;
  private Date dateField;
  private Date timestampField;

  public Data() {

  }

  public int getIntField() {

    return intField;
  }

  public void setIntField(int intField) {

    this.intField = intField;
  }

  public double getDecimalField() {

    return decimalField;
  }

  public void setDecimalField(double decimalField) {

    this.decimalField = decimalField;
  }

  public char getCharField() {

    return charField;
  }

  public void setCharField(char charField) {

    this.charField = charField;
  }

  public String getStringField() {

    return stringField;
  }

  public void setStringField(String stringField) {

    this.stringField = stringField;
  }

  public Date getDateField() {

    return dateField;
  }

  public void setDateField(Date dateField) {

    this.dateField = dateField;
  }

  public Date getTimestampField() {

    return timestampField;
  }

  public void setTimestampField(Date timestampField) {

    this.timestampField = timestampField;
  }
}
